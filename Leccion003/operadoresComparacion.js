let a = 3,
    b = 2,
    c = "3";

//operador de comparación (se revisa el valor sin importar el tipo)
let comparacion1 = a == b;
console.log(comparacion1);

//operador de comparación estricta (se revisa el valor y el tipo)
let comparacion2 = a === b;
console.log(comparacion2);

//operador Distinto A...
let comparacion3 = a != b;
console.log(comparacion3);

//operador Distinto A estricto
let comparacion4 = a !== c;
console.log(comparacion4);