//Operadores

let a = 3,
    b = 2;

//suma
let z = a + b;
console.log(`Resultado de la suma  ${z}`);

//resta
z = a - b
console.log(`El resultado de la resta es ${z} `);

//multiplicación
z = a * b;
console.log(`El resultado de la multiplicacion es ${z} `);

//division
z = a / b;
console.log(`El resultado de la división es ${z} `);

//Modulo
z = a % b;
console.log(`El resultado del moduelo es ${z} `);

//exponente
z = a ** b;
console.log(`El resultado de la exponenciación es ${z} `);

//incremento

let c = 9;
//Post-incremento (El operador ++ despues de la variable)
c++;
console.log(c);

//Pre-incremento (El operador ++ antes de la variable)
let d = 4,
    e = 3,
    x = d + e;
x = ++d;
console.log(d);
console.log(x);

//decremento

let y = 3,
    w = 2,
    suma = 3 - 2;

//Pre-Decremento
suma = --y;
console.log(y);
console.log(suma);

//Pos-Decremento
suma = w--;
console.log(w);
console.log(suma);